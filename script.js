let selectedUnits = [];
let currentQuestionIndex = 0;
let score = 0;
let quizQuestions = [];

document.getElementById('startQuiz').addEventListener('click', startQuiz);
document.getElementById('nextQuestion').addEventListener('click', nextQuestion);
document.getElementById('restartQuiz').addEventListener('click', restartQuiz);

function startQuiz() {
    selectedUnits = [];
    const units = document.querySelectorAll('.unit');
    let totalQuestions = 0;

    units.forEach(unit => {
        const unitId = unit.getAttribute('data-unit');
        const lessonCount = parseInt(unit.querySelector('.lessonCount').value);
        const grammarCount = parseInt(unit.querySelector('.grammarCount').value);

        if (lessonCount > 0) {
            selectedUnits.push({
                unitId,
                type: 'lesson',
                count: lessonCount
            });
        }
        if (grammarCount > 0) {
            selectedUnits.push({
                unitId,
                type: 'grammar',
                count: grammarCount
            });
        }

        totalQuestions += lessonCount + grammarCount;
    });

    if (selectedUnits.length === 0 || totalQuestions === 0) {
        alert('Please select at least one unit and number of questions.');
        return;
    }

    quizQuestions = generateQuizQuestions();
    document.getElementById('unitSelection').style.display = 'none';
    document.getElementById('startQuiz').style.display = 'none';
    document.getElementById('quizInterface').style.display = 'block';
    currentQuestionIndex = 0;
    score = 0;
    loadNextQuestion();
}

function generateQuizQuestions() {
    const questions = [];
    selectedUnits.forEach(unit => {
        const unitQuestions = allQuestions[unit.unitId][unit.type];
        const selectedQuestions = unitQuestions.sort(() => 0.5 - Math.random()).slice(0, unit.count);
        questions.push(...selectedQuestions);
    });

    const uniqueQuestions = [...new Set(questions.map(q => q.question))].map(q => questions.find(question => question.question === q));
    return uniqueQuestions.sort(() => 0.5 - Math.random());
}

function loadNextQuestion() {
    if (currentQuestionIndex < quizQuestions.length) {
        const question = quizQuestions[currentQuestionIndex];
        document.getElementById('questionTitle').textContent = question.question;
        const optionsDiv = document.getElementById('options');
        optionsDiv.innerHTML = '';

        question.options.forEach(option => {
            const optionContainer = document.createElement('div');
            optionContainer.className = 'option-container'; // استخدام الكلاس المناسب

            const button = document.createElement('button');
            button.textContent = option;
            button.className = 'option-button'; // إضافة كلاس للزر الخاص بالاختيارات
            button.addEventListener('click', () => checkAnswer(button, option, question.answer));

            const readButton = document.createElement('button');
            readButton.textContent = '🔊';
            readButton.className = 'speak-button'; // استخدام كلاس منفصل لزر الصوت
            readButton.addEventListener('click', () => readOptionText(option));

            optionContainer.appendChild(button);
            optionContainer.appendChild(readButton);
            optionsDiv.appendChild(optionContainer);
        });

        document.getElementById('feedback').textContent = '';
        document.getElementById('nextQuestion').style.display = 'none';
        updateProgress();
    } else {
        showResults();
    }
}

function readText(elementId) {
    const text = document.getElementById(elementId).textContent;
    const utterance = new SpeechSynthesisUtterance(text);
    utterance.lang = 'en-US';
    utterance.rate = 0.8;
    window.speechSynthesis.speak(utterance);
}

